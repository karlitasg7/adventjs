const { dropGifts } = require("../../src/2025/day20/solution");

describe("Day 20 - Vertical warehouse", () => {
  test("test 1", () => {
    expect(
      dropGifts(
        [
          [".", ".", "."],
          [".", "#", "."],
          ["#", "#", "."],
        ],
        [0]
      )
    ).toEqual([
      [".", ".", "."],
      ["#", "#", "."],
      ["#", "#", "."],
    ]);
  });

  test("test 2", () => {
    expect(
      dropGifts(
        [
          [".", ".", "."],
          ["#", "#", "."],
          ["#", "#", "#"],
        ],
        [0, 2]
      )
    ).toEqual([
      ["#", ".", "."],
      ["#", "#", "#"],
      ["#", "#", "#"],
    ]);
  });

  test("test 3", () => {
    expect(
      dropGifts(
        [
          [".", ".", "."],
          [".", ".", "."],
          [".", ".", "."],
        ],
        [0, 1, 2]
      )
    ).toEqual([
      [".", ".", "."],
      [".", ".", "."],
      ["#", "#", "#"],
    ]);
  });

  test("test 4", () => {
    expect(
      dropGifts(
        [
          ["#", "#"],
          ["#", "#"],
        ],
        [0, 0]
      )
    ).toEqual([
      ["#", "#"],
      ["#", "#"],
    ]);
  });
});
